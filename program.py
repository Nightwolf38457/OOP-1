# ============================================================
# Student Calculator - Version 1
# Description: Basic calculator with add and subtract
# ============================================================

def add(a, b):
    return a + b

def subtract(a, b):
    return a - b

print("=== Student Calculator v1 ===")
print("Addition:    5 + 3 =", add(5, 3))
print("Subtraction: 9 - 4 =", subtract(9, 4))
